// ── Генерация Младших Арканов (56 карт) ─────────────────

function buildMinorArcana(): TarotCardData[] {
  const suits: SuitKey[] = ["cups", "wands", "swords", "pentacles"];
  const cards: TarotCardData[] = [];

  for (const suit of suits) {
    for (let rank = 1; rank <= 14; rank++) {
      const suitData = suitNames[suit];
      const rankData = rankNames[rank];
      const kw = minorKeywords[suit][rank];

      cards.push({
        id: `${suit}-${rank}`,
        nameRu: `${rankData.ru} ${suitData.ru}`,
        nameEn: `${rankData.en} of ${suitData.en}`,
        arcana: "minor",
        suit,
        number: rank,
        keywordsUpright: kw.upright,
        keywordsReversed: kw.reversed,
        imageUrl: `${IMAGE_BASE}/minor/${suit}/${rank}.jpg`,
      });
    }
  }

  return cards;
}

